public class ProjectPerformanceIndexModel
{
    private String pv;
    private String ev;
    private String ac;
    private String cpi;
    private String spi;

    public String getPv()
    {
        return pv;
    }

    public void setPv(String pv)
    {
        this.pv = pv;
    }

    public String getEv()
    {
        return ev;
    }

    public void setEv(String ev)
    {
        this.ev = ev;
    }

    public String getAc()
    {
        return ac;
    }

    public void setAc(String ac)
    {
        this.ac = ac;
    }

    public String getCpi()
    {
        return cpi;
    }

    public void setCpi(String cpi)
    {
        this.cpi = cpi;
    }

    public String getSpi()
    {
        return spi;
    }

    public void setSpi(String spi)
    {
        this.spi = spi;
    }
}
